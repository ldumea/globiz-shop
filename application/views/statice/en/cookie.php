			            <p><u>Ce sunt cookie-urile folosite pe acest site web?</u></p>
			            <p>Cookie-urile reprezintă o parte esențială a funcționării site-ului nostru web. Scopul principal al cookie-urilor noastre este de a vă îmbunătăți experiența de navigare. De exemplu, ele sunt folosite pentru a ne aminti preferințele (limba, țara etc.) în timpul navigării și vizitelor viitoare.</p>
			            <p>Informațiile colectate de fișierele cookie ne permit, de asemenea, să îmbunătățim site-ul web prin estimarea numerelor și tiparelor de utilizare, adecvarea site-ului web la interesele individuale ale utilizatorilor, căutări mai rapide etc.</p>
			            <p>în unele cazuri, dacă am obținut în prealabil consimțământul dvs. informat, este posibil să folosim cookie-uri, tag-uri sau alte dispozitive similare pentru a obține informații care ne permit să vă prezentăm, fie de pe site-ul nostru web propriu, fie de pe site-uri web terțe sau prin orice alt mijloc, publicitate pe baza analizei obiceiurilor dumneavoastră de navigare.</p>
