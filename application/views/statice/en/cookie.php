			            <p><u>Ce este un cookie?</u></p>
			            <p>Un cookie este un mic fișier text pe care un site web îl stochează pe PC-ul dumneavoastră, pe telefon sau pe orice alt dispozitiv, cu informații despre navigarea pe site-ul web respectiv. Cookie-urile sunt necesare pentru a facilita navigarea și a o face mai ușor de utilizat și nu dăunează computerului.</p>
